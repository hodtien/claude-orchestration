#!/usr/bin/env bash
# sprint-retrospective.sh — Sprint retrospective ceremony
# Usage: sprint-retrospective.sh <sprint-id>
#
# Gathers team input and produces Claude prompts for automated retrospective.

set -euo pipefail

SPRINT_ID="${1:?Usage: sprint-retrospective.sh <sprint-id>}"
ORCH_DIR="${PROJECT_ROOT:-.}/.orchestration"
DATE=$(date +%Y-%m-%d)

echo ""
echo "🔄 Sprint Retrospective — $SPRINT_ID"
echo "══════════════════════════════════════"
echo "Date: $DATE"
echo ""

# ── Collect human input ───────────────────────────────────────────────────────
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "👤 Your input (optional — press Enter to skip any):"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo ""
read -rp "✅ What went well this sprint? " WENT_WELL
read -rp "❌ What didn't go well? " WENT_BADLY
read -rp "🎯 Top action item for next sprint? " ACTION_ITEM
echo ""

# ── Save to file ──────────────────────────────────────────────────────────────
RETRO_FILE="$ORCH_DIR/sprints/${SPRINT_ID}-retro.md"
mkdir -p "$ORCH_DIR/sprints"
cat > "$RETRO_FILE" << EOF
---
sprint_id: $SPRINT_ID
retro_date: $DATE
---

# Sprint Retrospective — $SPRINT_ID

## ✅ What Went Well
${WENT_WELL:-"(to be filled by Claude analysis)"}

## ❌ What Didn't Go Well
${WENT_BADLY:-"(to be filled by Claude analysis)"}

## 🎯 Action Items
- ${ACTION_ITEM:-"(to be generated from analysis)"}

## 📊 Metrics Analysis
(generated by Claude from Memory Bank)
EOF
echo "✅ Retro notes saved: $RETRO_FILE"
echo ""

# ── Claude prompts ────────────────────────────────────────────────────────────
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "📋 PASTE INTO CLAUDE for full retrospective:"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo ""
echo "1️⃣  Analyze sprint performance:"
echo "   \"Memory bank: get sprint report for $SPRINT_ID. Then analyze: what patterns indicate the team worked well, and where were the bottlenecks?\""
echo ""
echo "2️⃣  Generate action items:"
echo "   \"Based on sprint $SPRINT_ID metrics and this feedback — Went well: ${WENT_WELL:-N/A}. Went badly: ${WENT_BADLY:-N/A} — generate concrete, prioritized action items for the next sprint.\""
echo ""
echo "3️⃣  Store insights:"
echo "   \"Memory bank: store retrospective insights for $SPRINT_ID as knowledge article 'retrospectives/$SPRINT_ID'\""
echo ""
echo "4️⃣  Archive sprint:"
echo "   \"Memory bank: archive sprint $SPRINT_ID\""
echo ""
